        for (int j = 0; j < evolutions.GetLength(1); j++)
        {
            if (current.Count == 0 || evolutions[0, j, 0] != "0" && evolutions[0, j, 1] != "0" && current[current.Count - 1].name != evolutions[0, j, 1])
            {
                level.Add(evolutions[0, j, 1]);
                current.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[0, j, 0] + ".prefab", typeof(GameObject)) as GameObject);
                currentShiny.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[0, j, 0] + "Shiny.prefab", typeof(GameObject)) as GameObject);
                next.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[1, j, 0] + ".prefab", typeof(GameObject)) as GameObject);
                nextShiny.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[0 + 1, j, 0] + "Shiny.prefab", typeof(GameObject)) as GameObject);
            }
        }
        for (int j = 0; j < evolutions.GetLength(1); j++)
        {
            if (current.Count == 0 || evolutions[1, j, 0] != "0" && evolutions[1, j, 1] != "0" && current[current.Count - 1].name != evolutions[1, j, 1])
