                level.Add(evolutions[1, j, 1]);
                current.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[1, j, 0] + ".prefab", typeof(GameObject)) as GameObject);
                currentShiny.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[1, j, 0] + "Shiny.prefab", typeof(GameObject)) as GameObject);
                next.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[2, j, 0] + ".prefab", typeof(GameObject)) as GameObject);
                nextShiny.Add(AssetDatabase.LoadAssetAtPath("Assets/Resources/Pokemon/Prefabs/" + evolutions[1 + 1, j, 0] + "Shiny.prefab", typeof(GameObject)) as GameObject);
            }
        }
        for (int j = 0; j < current.Count; j++)
        {
            if (next[j] != null)
            {
                current[j].GetComponent<BasePokemon>().canEvolve = true;
                currentShiny[j].GetComponent<BasePokemon>().canEvolve = true;

                current[j].GetComponent<BasePokemon>().evolveTo.levelUp = level[j];
                currentShiny[j].GetComponent<BasePokemon>().evolveTo.levelUp = level[j];

                current[j].GetComponent<BasePokemon>().evolveTo.nextEvolution = next[j].GetComponent<BasePokemon>();
                currentShiny[j].GetComponent<BasePokemon>().evolveTo.nextEvolution = nextShiny[j].GetComponent<BasePokemon>();
            }
            GameObject temp = Instantiate(current[j]);
            GameObject tempShiny = Instantiate(currentShiny[j]);
            PrefabUtility.SaveAsPrefabAsset(temp, "Assets/Resources/Pokemon/Prefabs/" + current[j].name + ".prefab");
            PrefabUtility.SaveAsPrefabAsset(tempShiny, "Assets/Resources/Pokemon/Prefabs/" + currentShiny[j].name + ".prefab");

            Destroy(temp);
            Destroy(tempShiny);
        }
    }
